<script>
    function article() {
        $.post('/hzzadmin/<?echo $slider_tag;?>/role', {
                id: $('#id').val()
            },
            function (res) {
                if (res.status != 'false') {
                    $('#rolename').val(res.data.rolename);
                }
            }, 'json');
    }
    article();
    $('#save').bind('click', function(){
        $.ajax({
            url:"/hzzadmin/<?echo $slider_tag;?>/roleUpdate",
            data:$("#datares").serialize(),
            type:"post",
            success:function(data){//ajax返回的数据
                if (data.status=='false')
                {
                    if (data.code == '869'){
                        $('#token').val(data.token);
                    }
                } else  {
                        layer.msg('数据修改成功');
                    window.history.back(-1);
                }
            }
        });
    })
    $('#backbtn').bind('click', function(){
        window.history.back(-1);
    })
